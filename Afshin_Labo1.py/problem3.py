def diviser_par_3_ou_3():
  
    Nombre=int(input("Entrez un nombre : "))


    if(Nombre%2==0):
        print("ce nombre est divisible par 2 :" ,Nombre )

    elif(Nombre%3==0):
        print("ce nombre est divisible par 3 :" ,Nombre )   

    else:
        print(Nombre)


diviser_par_3_ou_3()

